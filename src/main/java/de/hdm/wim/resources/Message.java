public class Message {

    private int             _messageId;
    private Enums.Origin    _messageOrigin;
    private Enums.Topic     _messageTopic;
    private Event           _event;

    /**
     * Instantiates a new Message.
     *
     * @param _messageId     the message id
     * @param _messageOrigin the message origin
     * @param _messageTopic  the message topic
     * @param _event         the event
     */
    public Message(int _messageId, Enums.Origin _messageOrigin, Enums.Topic _messageTopic, Event _event) {
        this._messageId = _messageId;
        this._messageOrigin = _messageOrigin;
        this._messageTopic = _messageTopic;
        this._event = _event;
    }

    /**
     * Gets message id.
     *
     * @return the message id
     */
    public int get_messageId() {
        return _messageId;
    }

    /**
     * Sets message id.
     *
     * @param _messageId the message id
     */
    public void set_messageId(int _messageId) {
        this._messageId = _messageId;
    }

    /**
     * Gets message origin.
     *
     * @return the message origin
     */
    public Enums.Origin get_messageOrigin() {
        return _messageOrigin;
    }

    /**
     * Sets message origin.
     *
     * @param _messageOrigin the message origin
     */
    public void set_messageOrigin(Enums.Origin _messageOrigin) {
        this._messageOrigin = _messageOrigin;
    }

    /**
     * Gets message topic.
     *
     * @return the message topic
     */
    public Enums.Topic get_messageTopic() {
        return _messageTopic;
    }

    /**
     * Sets message topic.
     *
     * @param _messageTopic the message topic
     */
    public void set_messageTopic(Enums.Topic _messageTopic) {
        this._messageTopic = _messageTopic;
    }

    /**
     * Gets event.
     *
     * @return the event
     */
    public Event get_event() {
        return _event;
    }

    /**
     * Sets event.
     *
     * @param _event the event
     */
    public void set_event(Event _event) {
        this._event = _event;
    }
}
